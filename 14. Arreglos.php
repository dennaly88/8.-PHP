<?php

// 1. Se crea un arreglo asociativo llamado $rutinas.
$rutinas = array(
    "lunes" => "Pecho, Espalda, Hombros",
    "martes" => "Piernas, Glúteos",
    "miercoles" => "Bíceps, Espalda, Hombros",
    "jueves" => "Tríceps, Bíceps",
    "viernes" => "Piernas, Pantorrillas"
);

// 2. Se imprimen los resultados para cada día.
// Se puede usar un bucle foreach para recorrer todas las rutinas.
echo "¡Tu rutina de ejercicios de esta semana es la siguiente! 🏋️\n\n";

foreach ($rutinas as $dia => $ejercicios) {
    // La primera letra del día se pone en mayúscula para una mejor presentación.
    echo ucfirst($dia) . ": Tienes rutina de " . $ejercicios . ".\n";
}

?>
