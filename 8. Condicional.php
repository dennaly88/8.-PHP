            <h3>Condicionales en PHP</h3>
            <hr>
            <br>
            <h2>IF</h2>
            <p>La estructura if es una estructura de control que permite ejecutar un bloque de código
            si una condición es verdadera. Si la condición es falsa, el bloque de código no se ejecuta.</p>
            <br>
            <h3>Sintaxis</h3>
            <pre>
if (condición) {
    // Código a ejecutar si la condición es verdadera
}
            </pre>
            <br>
            <h3>Ejemplo</h3>
            <pre>
$edad = 18;
if ($edad >= 18) {
    echo "Eres mayor de edad.";
}
            </pre>

            <?php
                $edad = 18;
                if ($edad >= 18) {
                    echo "Eres mayor de edad.";
                }
            ?>
            <br>
            <h2>IF...ELSE</h2>
            <p>La estructura if...else es una extensión de la estructura if que permite ejecutar
            un bloque de código si una condición es verdadera y otro bloque de código si la condición es falsa.</p>
            <br>
            <h3>Sintaxis</h3>
            <pre>               
if (condición) {
    // Código a ejecutar si la condición es verdadera
} else {
    // Código a ejecutar si la condición es falsa
}
            </pre>
            <?php
                $edad = 16;
                if ($edad >= 18) {
                    echo "Eres mayor de edad.";
                } else {
                    echo "Eres menor de edad.";
                }
?>
            <br>    
            <h3>Ejemplo</h3>
            <pre>
$edad = 16;
if ($edad >= 18) {
    echo "Eres mayor de edad.";
} else {
    echo "Eres menor de edad.";
}
            </pre>
            <?php
                $edad = 16;
                if ($edad >= 18) {
                    echo "Eres mayor de edad.";
                } else {
                    echo "Eres menor de edad.";
                }
            ?>
            <br>
            <h2>IF...ELSEIF...ELSE</h2>
            <p>La estructura if...elseif...else es una extensión de la estructura if...else que permite
            evaluar múltiples condiciones y ejecutar diferentes bloques de código según cuál condición sea verdadera.</p>
            <br>
            <h3>Sintaxis</h3>
            <pre>               
if (condición1) {
    // Código a ejecutar si la condición1 es verdadera
} elseif (condición2) {
    // Código a ejecutar si la condición2 es verdadera
} else {
    // Código a ejecutar si ninguna de las condiciones anteriores es verdadera
}
            </pre>
            <?php
                $nota = 85;
                if ($nota >= 90) {
                    echo "Sobresaliente";
                } elseif ($nota >= 80) {
                    echo "Notable";
                } elseif ($nota >= 70) {
                    echo "Aprobado";
                } else {
                    echo "Suspenso";
                }
            ?>
            <br>    
