<?php
    $numero = 10;
    echo "Valor inicial: ".$numero;
    echo "<br>";

    $numero = 20;
    echo "Después de asignación simple (\$numero = 20): ".$numero;
    echo "<br>";


    $numero += 5; 
    echo "Después de asignación con suma (\$numero += 5): ".$numero;
    echo "<br>";


    $numero -= 3; 
    echo "Después de asignación con resta (\$numero -= 3): ".$numero;
    echo "<br>";


    $numero *= 2; 
    echo "Después de asignación con multiplicación (\$numero *= 2): ".$numero;
    echo "<br>";

    // Asignación con división
    $numero /= 4; // Equivalente a $numero = $numero / 4
    echo "Después de asignación con división (\$numero /= 4): ".$numero;
    echo "<br>";

    // Asignación con módulo
    $numero %= 3; // Equivalente a $numero = $numero % 3
    echo "Después de asignación con módulo (\$numero %= 3): ".$numero;
    echo "<br>";
    echo "<br>";    


?>

