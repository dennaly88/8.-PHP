<?php
    $verdadero = true;
    $falso = false;

    echo "Valor Verdadero: ";
    var_dump($verdadero);
    echo "<br>";
    echo "Valor Falso: ";
    var_dump($falso);
    echo "<br>";
    echo "<br>";


    echo "Operador AND (&&): <br>";
    var_dump($verdadero && $falso); 
    echo "<br>";
    var_dump($verdadero && $verdadero); 
    echo "<br>";
    var_dump($falso && $falso); 
    echo "<br>";
    echo "<br>";

    
    echo "Operador OR (||): <br>";
    var_dump($verdadero || $falso); 
    echo "<br>";
    var_dump($verdadero || $verdadero); 
    echo "<br>";
    var_dump($falso || $falso); 
    echo "<br>";
    echo "<br>";

    
    echo "Operador NOT (!): <br>";
    var_dump(!$verdadero); 
    echo "<br>";
    var_dump(!$falso); 
    echo "<br>";
    echo "<br>";    
    

?>

